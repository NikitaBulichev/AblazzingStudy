package Homework2;

public class Cat {

    String color;

    int age;

    public Cat(String color, int age) {

        this.color = color;

        this.age = age;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
