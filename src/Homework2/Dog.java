package Homework2;

public class Dog {

    String eyecolor;

    int age;

    public Dog(String eyecolor, int age) {

        this.eyecolor = eyecolor;

        this.age = age;
    }

    public String getEyecolor() {
        return eyecolor;
    }

    public void setEyecolor(String eyecolor) {
        this.eyecolor = eyecolor;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
